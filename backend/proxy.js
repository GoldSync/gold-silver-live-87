function isMarketOpen(settings) {
    const now = new Date();
    const day = now.getUTCDay();     // 0=Sun, 6=Sat
    const hour = now.getUTCHours();
    const minute = now.getUTCMinutes();
    const currentTotalMinutes = hour * 60 + minute;

    // Default values in UTC
    // Friday 23:58 AST = 20:58 UTC (1258 mins)
    // Monday 02:01 AST = Sunday 23:01 UTC (1381 mins)
    const closeStr = String(settings?.marketCloseUTC || '20:58');
    const openStr = String(settings?.marketOpenUTC || '23:01');

    const [closeH, closeM] = closeStr.includes(':') ? closeStr.split(':').map(Number) : [Number(closeStr), 0];
    const [openH, openM] = openStr.includes(':') ? openStr.split(':').map(Number) : [Number(openStr), 0];

    // Ensure they are valid numbers
    const validCloseH = isNaN(closeH) ? 20 : closeH;
    const validCloseM = isNaN(closeM) ? 58 : closeM;
    const validOpenH = isNaN(openH) ? 23 : openH;
    const validOpenM = isNaN(openM) ? 1 : openM;

    const closeMinutes = validCloseH * 60 + validCloseM;
    const openMinutes = validOpenH * 60 + validOpenM;

    // Forex/Gold markets close Friday ~UTC, reopen Sunday ~UTC
    if (day === 6) return false;                 // All Saturday
    if (day === 0 && currentTotalMinutes < openMinutes) return false;    // Sunday before open
    if (day === 5 && currentTotalMinutes >= closeMinutes) return false;   // Friday after close
    return true;
}

async function scrapeClosingPrices() {
    console.log('[WEEKEND] Scraping TradingView for closing prices...');
    let tvPage = null;
    try {
        await initBrowser();
        tvPage = await browser.newPage();
        await tvPage.setUserAgent('Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36');

        // Scrape Gold
        await tvPage.goto('https://www.tradingview.com/symbols/XAUUSD/', { waitUntil: 'networkidle2', timeout: 30000 });
        const goldData = await tvPage.evaluate(() => {
            const priceEl = document.querySelector('.js-symbol-last');
            const changeEl = document.querySelector('.js-symbol-change-direction');
            const timeEl = document.querySelector('.js-symbol-lp-time');
            const price = priceEl ? parseFloat(priceEl.innerText.trim().replace(/,/g, '')) : 0;
            let changeText = '';
            if (changeEl) {
                const spans = changeEl.querySelectorAll('span');
                changeText = spans.length > 1 ? spans[1].innerText.trim() : '';
            }
            const closeInfo = timeEl ? timeEl.innerText.trim() : '';
            return { price, changeText, closeInfo };
        });

        // Scrape Silver
        await tvPage.goto('https://www.tradingview.com/symbols/XAGUSD/', { waitUntil: 'networkidle2', timeout: 30000 });
        const silverData = await tvPage.evaluate(() => {
            const priceEl = document.querySelector('.js-symbol-last');
            const changeEl = document.querySelector('.js-symbol-change-direction');
            const price = priceEl ? parseFloat(priceEl.innerText.trim().replace(/,/g, '')) : 0;
            let changeText = '';
            if (changeEl) {
                const spans = changeEl.querySelectorAll('span');
                changeText = spans.length > 1 ? spans[1].innerText.trim() : '';
            }
            return { price, changeText };
        });

        await tvPage.close();
        tvPage = null;

        if (goldData.price && silverData.price) {
            console.log(`[WEEKEND] TradingView closing prices: Gold=$${goldData.price}, Silver=$${silverData.price}`);
            console.log(`[WEEKEND] Close info: ${goldData.closeInfo}`);

            // Persist to MongoDB
            if (mongoose.connection.readyState === 1) {
                await ClosingPrice.findOneAndUpdate({}, {
                    gold_price: goldData.price,
                    silver_price: silverData.price,
                    closeDate: goldData.closeInfo,
                    scrapedAt: new Date()
                }, { upsert: true });
            }

            return {
                gold_price: goldData.price,
                silver_price: silverData.price,
                closeDate: goldData.closeInfo
            };
        } else {
            console.warn('[WEEKEND] TradingView scrape returned 0s');
            return null;
        }

    } catch (error) {
        console.error('[WEEKEND] TradingView scrape error:', error.message);
        if (tvPage) {
            try { await tvPage.close(); } catch (e) { }
        }
        return null;
    }
}

async function initBrowser() {
    if (!browser) {
        console.log("Launching shared browser...");
        browser = await puppeteer.launch({
            headless: "new",
            args: [
                '--no-sandbox',
                '--disable-setuid-sandbox',
                '--disable-dev-shm-usage', // Docker/Container fix
                '--disable-gpu'
            ],
            ignoreHTTPSErrors: true
        });

        goldPage = await browser.newPage();
        await goldPage.setUserAgent('Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36');

        silverPage = await browser.newPage();
        await silverPage.setUserAgent('Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36');

        const goldUrl = process.env.SCRAPE_URL_GOLD || 'https://www.livepriceofgold.com/usa-gold-price.html';
        const silverUrl = process.env.SCRAPE_URL_SILVER || 'https://www.livepriceofgold.com/silver-price/us.html';

        console.log("Navigating tabs...");
        await Promise.all([
            goldPage.goto(goldUrl, { waitUntil: 'domcontentloaded', timeout: 60000 }),
            silverPage.goto(silverUrl, { waitUntil: 'domcontentloaded', timeout: 60000 })
        ]);
        console.log("Tabs ready.");
        lastReloadTime = Date.now();
    }
}

async function scrapeLoop() {
    if (isScraping) return;
    isScraping = true;

    let settings = null;
    try {
        if (mongoose.connection.readyState === 1) {
            settings = await Settings.findOne();
        }
    } catch (err) {
        console.warn('[SCRAPE] Failed to fetch settings, using defaults.');
    }

    try {
        // ==========================================
        // WEEKEND MODE: Serve frozen closing prices
        // ==========================================
        if (!isMarketOpen(settings)) {
            if (!weekendClosingFetched) {
                console.log('[WEEKEND] Market is closed. Fetching closing prices...');

                // Try TradingView scrape first
                const tvData = await scrapeClosingPrices();

                if (tvData) {
                    cachedPrices = {
                        price: tvData.gold_price,
                        bid: tvData.gold_price,
                        ask: tvData.gold_price,
                        silver_price: tvData.silver_price,
                        silver_bid: tvData.silver_price,
                        silver_ask: tvData.silver_price,
                        price_gram_24k: tvData.gold_price / 31.1035,
                        price_gram_22k: (tvData.gold_price / 31.1035) * 0.916,
                        price_gram_21k: (tvData.gold_price / 31.1035) * 0.875,
                        price_gram_18k: (tvData.gold_price / 31.1035) * 0.750,
                        timestamp: new Date().toISOString(),
                        isWeekend: true,
                        closeDate: tvData.closeDate || 'Friday Close'
                    };
                    weekendClosingFetched = true;
                    console.log('[WEEKEND] Closing prices cached successfully.');
                } else {
                    // Fallback: try loading from MongoDB
                    console.log('[WEEKEND] TradingView scrape failed, trying MongoDB...');
                    if (mongoose.connection.readyState === 1) {
                        const saved = await ClosingPrice.findOne().sort({ scrapedAt: -1 });
                        if (saved) {
                            cachedPrices = {
                                price: saved.gold_price,
                                bid: saved.gold_price,
                                ask: saved.gold_price,
                                silver_price: saved.silver_price,
                                silver_bid: saved.silver_price,
                                silver_ask: saved.silver_price,
                                price_gram_24k: saved.gold_price / 31.1035,
                                price_gram_22k: (saved.gold_price / 31.1035) * 0.916,
                                price_gram_21k: (saved.gold_price / 31.1035) * 0.875,
                                price_gram_18k: (saved.gold_price / 31.1035) * 0.750,
                                timestamp: new Date().toISOString(),
                                isWeekend: true,
                                closeDate: saved.closeDate || 'Friday Close'
                            };
                            weekendClosingFetched = true;
                            console.log('[WEEKEND] Loaded closing prices from MongoDB.');
                        }
                    }
                }
            }
            // Poll infrequently on weekends (every 60s to detect Monday reopen)
            return;
        }

        // ==========================================
        // WEEKDAY MODE: Normal live scraping
        // ==========================================
        // Reset weekend flag when market reopens
        if (weekendClosingFetched) {
            weekendClosingFetched = false;
            console.log('[WEEKDAY] Market reopened. Resuming live scraping...');
        }

        await initBrowser();

        const timestamp = Date.now();

        // Safety: Full reload every 5 minutes (300000ms) to clear memory/stuck scripts
        if (timestamp - lastReloadTime > 300000) {
            console.log("Performing periodic page reload...");
            await Promise.all([
                goldPage.reload({ waitUntil: 'domcontentloaded', timeout: 30000 }),
                silverPage.reload({ waitUntil: 'domcontentloaded', timeout: 30000 })
            ]);
            lastReloadTime = timestamp;
        }

        // Logic Change: DO NOT RELOAD EVERY TIME!
        // Just read the DOM. The site updates via JS WebSocket/Long-polling.

        if (goldPage.isClosed() || silverPage.isClosed()) {
            throw new Error("Page was closed or detached");
        }

        // Extract Gold
        const goldData = await goldPage.evaluate(() => {
            const getPrice = (selector) => {
                const el = document.querySelector(selector);
                return el ? parseFloat(el.innerText.trim().replace(/,/g, '')) : 0;
            };
            return {
                price: getPrice('[data-price="XAUUSD"]'),
                bid: getPrice('[data-price="XAUUSD_BID"]'),
                ask: getPrice('[data-price="XAUUSD_ASK"]')
            };
        });

        // Extract Silver
        const silverData = await silverPage.evaluate(() => {
            const getPrice = (selector) => {
                const el = document.querySelector(selector);
                return el ? parseFloat(el.innerText.trim().replace(/,/g, '')) : 0;
            };
            return {
                price: getPrice('[data-price="XAGUSD"]'),
                bid: getPrice('[data-price="XAGUSD_BID"]'),
                ask: getPrice('[data-price="XAGUSD_ASK"]')
            };
        });

        // Update Cache
        if (goldData.price || silverData.price) {
            // Only log if values changed (reduce noise)
            const oldPrice = cachedPrices ? cachedPrices.price : 0;
            if (goldData.price !== oldPrice) {
                console.log(`[${new Date().toLocaleTimeString()}] PRICE UPDATE: Gold=${goldData.price}, Silver=${silverData.price}`);
            } else {
                console.log("No change..."); // Optional debug
            }

            cachedPrices = {
                price: goldData.price,
                bid: goldData.bid,
                ask: goldData.ask,
                silver_price: silverData.price,
                silver_bid: silverData.bid,
                silver_ask: silverData.ask,
                price_gram_24k: goldData.price / 31.1035,
                price_gram_22k: (goldData.price / 31.1035) * 0.916,
                price_gram_21k: (goldData.price / 31.1035) * 0.875,
                price_gram_18k: (goldData.price / 31.1035) * 0.750,
                timestamp: new Date().toISOString(),
                isWeekend: false
            };
            consecutiveScrapeFailures = 0; // Reset on success
        } else {
            console.warn('[SCRAPE] Extracted values are 0 or null, keeping old cache.');
            consecutiveScrapeFailures++;
        }
    } catch (error) {
        console.error('[SCRAPE] Error during scrape loop:', error.message);
        consecutiveScrapeFailures++;
        if (consecutiveScrapeFailures >= MAX_SCRAPE_FAILURES) {
            sendErrorAlert(
                'Live Scraper Failure',
                `The live gold/silver price scraper has failed ${consecutiveScrapeFailures} times in a row. Final check: ${error.message}`,
                'scrape-fail'
            );
            console.error(`[SCRAPE] Max consecutive scrape failures reached. Restarting browser.`);
            if (browser) await browser.close().catch(() => { });
            browser = null; // Force re-initialization
        }
    } finally {
        isScraping = false;
        // Weekend: poll every 60s; Weekday: poll every 1s
        const nextInterval = isMarketOpen(settings) ? 1000 : 60000;
        setTimeout(scrapeLoop, nextInterval);
    }
}

