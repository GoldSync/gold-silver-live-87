app.post('/api/auth/login', async (req, res) => {
    const { username, password } = req.body;

    try {
        const userCount = await User.countDocuments();
        if (userCount === 0) {
            // No users exist - this happens before onboarding.
            // We allow admin/admin ONLY if onboarding isn't done yet and it's a fresh install.
            let onboarding = await Onboarding.findOne();
            if (!onboarding || !onboarding.completed) {
                if (username === 'admin' && password === 'admin') {
                    const hash = await bcrypt.hash('admin', 10);
                    await new User({ username: 'admin', passwordHash: hash }).save();
                    console.log('Seeded initial fallback admin user.');
                }
            }
        }

        const user = await User.findOne({ username });
        if (!user) return res.status(400).json({ error: 'Invalid credentials' });

        const validPassword = await bcrypt.compare(password, user.passwordHash);
        if (!validPassword) return res.status(400).json({ error: 'Invalid credentials' });

        const token = jwt.sign({ id: user._id, username: user.username }, JWT_SECRET, { expiresIn: '24h' });
        console.log(`[LOGIN] User ${username} logged in. adminName: ${user.adminName}`);
        res.json({
            token,
            username: user.username,
            adminName: user.adminName || '',
            adminEmail: user.adminEmail || ''
        });
    } catch (err) {
        res.status(500).json({ error: 'Server error', details: err.message });
    }
});

