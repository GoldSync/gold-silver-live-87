// Throttling for alerts (1 email per hour per error type)
const alertCache = new Map();
const ALERT_THROTTLE_MS = 60 * 60 * 1000;

async function sendErrorAlert(subject, message, type = 'general') {
    const lastSent = alertCache.get(type);
    const now = Date.now();

    if (lastSent && (now - lastSent) < ALERT_THROTTLE_MS) {
        console.log(`[ALERT] Throttling alert: ${type} (Last sent ${Math.round((now - lastSent) / 1000 / 60)} mins ago)`);
        return;
    }

    try {
        // Fetch Admin Email
        let adminEmail = process.env.GMAIL_USER; // fallback
        if (mongoose.connection.readyState === 1) {
            const admin = await User.findOne({ username: 'admin' });
            if (admin?.adminEmail) adminEmail = admin.adminEmail;
        }

        const mailOptions = {
            from: `"GoldSync System Monitor" <${process.env.GMAIL_USER}>`,
            to: adminEmail,
            subject: `[SYSTEM ALERT] ${subject}`,
            html: `
                <div style="font-family: sans-serif; padding: 20px; border: 2px solid #ff4444; border-radius: 10px;">
                    <h2 style="color: #ff4444;">Critical System Alert</h2>
                    <p style="font-size: 1.1em;"><strong>Issue:</strong> ${message}</p>
                    <hr />
                    <p style="font-size: 0.8em; color: #666;">Generated by GoldSync Proxy at ${new Date().toLocaleString()}</p>
                </div>
            `
        };

        await transporter.sendMail(mailOptions);
        alertCache.set(type, now);
        console.log(`[ALERT] Error email sent: ${subject}`);
    } catch (err) {
        console.error('[ALERT] Failed to send error alert:', err.message);
    }
}

