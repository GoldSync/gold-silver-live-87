function isMarketOpen(settings) {
    const now = new Date();
    const day = now.getUTCDay();     // 0=Sun, 6=Sat
    const hour = now.getUTCHours();
    const minute = now.getUTCMinutes();
    const currentTotalMinutes = hour * 60 + minute;

    // Default values in UTC
    // Friday 23:58 AST = 20:58 UTC (1258 mins)
    // Monday 02:01 AST = Sunday 23:01 UTC (1381 mins)
    const closeStr = String(settings?.marketCloseUTC || '20:58');
    const openStr = String(settings?.marketOpenUTC || '23:01');

    const [closeH, closeM] = closeStr.includes(':') ? closeStr.split(':').map(Number) : [Number(closeStr), 0];
    const [openH, openM] = openStr.includes(':') ? openStr.split(':').map(Number) : [Number(openStr), 0];

    // Ensure they are valid numbers
    const validCloseH = isNaN(closeH) ? 20 : closeH;
    const validCloseM = isNaN(closeM) ? 58 : closeM;
    const validOpenH = isNaN(openH) ? 23 : openH;
    const validOpenM = isNaN(openM) ? 1 : openM;

    const closeMinutes = validCloseH * 60 + validCloseM;
    const openMinutes = validOpenH * 60 + validOpenM;

    // Forex/Gold markets close Friday ~UTC, reopen Sunday ~UTC
    if (day === 6) return false;                 // All Saturday
    if (day === 0 && currentTotalMinutes < openMinutes) return false;    // Sunday before open
    if (day === 5 && currentTotalMinutes >= closeMinutes) return false;   // Friday after close
    return true;
}

