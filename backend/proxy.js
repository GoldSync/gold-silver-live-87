app.put('/api/settings', authenticateToken, async (req, res) => {
    try {
        let settings = await Settings.findOne();
        if (!settings) {
            settings = new Settings();
        }

        if (req.body.categoryTitles) {
            settings.categoryTitles = { ...settings.categoryTitles, ...req.body.categoryTitles };
        }
        if (req.body.margin !== undefined) {
            settings.margin = req.body.margin;
        }
        if (req.body.marginType !== undefined) {
            settings.marginType = req.body.marginType;
        }
        if (req.body.spotMargin !== undefined) {
            settings.spotMargin = req.body.spotMargin;
        }
        if (req.body.isLocked !== undefined) {
            settings.isLocked = req.body.isLocked;
        }
        if (req.body.currencyRate !== undefined) {
            settings.currencyRate = req.body.currencyRate;
        }
        if (req.body.marketCloseUTC !== undefined) {
            settings.marketCloseUTC = req.body.marketCloseUTC;
        }
        if (req.body.marketOpenUTC !== undefined) {
            settings.marketOpenUTC = req.body.marketOpenUTC;
        }

        await settings.save();
        res.json(settings);
    } catch (err) {
        res.status(500).json({ error: 'Failed to update settings', details: err.message });
    }
});

